
public class Main {
    public static void main(String[] args) {

        int mes = 2;

        switch (mes) {
            case 1:
                System.out.println("Jan");
                break;
            case 2:
                System.out.println("Fev");
                break;
            case 3:
                System.out.println("Mar");
                break;
            default:
                System.out.println("Mes Invalido");

        }

    }
}